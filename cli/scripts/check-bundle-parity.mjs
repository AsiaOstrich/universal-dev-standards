#!/usr/bin/env node

/**
 * Bundle ⇄ Source Parity Check
 *
 * Enforces that every `.ai.yaml` standard under the repo-root `.standards/`
 * directory also appears in the npm-bundled copy at `cli/bundled/ai/`
 * (and vice versa), unless explicitly listed in `bundle-exclude.json`.
 *
 * Motivation: DEC-044 / XSPEC-071. When bundled drifts from source, running
 * `uds update` in any adopter project (or worse, in the UDS source repo
 * itself) causes source-of-truth loss.
 *
 * Usage:
 *   node cli/scripts/check-bundle-parity.mjs           # run check, exit 0/1
 *   node cli/scripts/check-bundle-parity.mjs --json    # JSON output
 *
 * Exit codes:
 *   0 — bundled and source match (modulo bundle-exclude.json)
 *   1 — parity mismatch detected (details printed to stdout)
 *   2 — internal error (missing source dir, invalid exclude file, etc.)
 *
 * Note: `cli/bundled/` is generated by `cli/scripts/prepack.mjs` and is
 * gitignored. In CI the check should either run `npm run prepack` first
 * or run against a freshly packed tree. If `cli/bundled/` is absent this
 * script exits 2 with guidance.
 */

import { existsSync, readFileSync, readdirSync, statSync } from 'fs';
import { dirname, join, relative } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const CLI_ROOT = join(__dirname, '..');
const REPO_ROOT = join(CLI_ROOT, '..');
const SOURCE_DIR = join(REPO_ROOT, '.standards');
const BUNDLED_AI_DIR = join(CLI_ROOT, 'bundled', 'ai');
const EXCLUDE_CONFIG = join(__dirname, 'bundle-exclude.json');

const args = new Set(process.argv.slice(2));
const JSON_OUTPUT = args.has('--json');

/**
 * Recursively collect all `.ai.yaml` file paths under `root`, returning
 * paths relative to `root` and forward-slash-normalized for stable compare.
 */
function collectAiYaml(root) {
  /** @type {string[]} */
  const results = [];
  if (!existsSync(root)) return results;

  function walk(currentDir) {
    const entries = readdirSync(currentDir);
    for (const entry of entries) {
      const full = join(currentDir, entry);
      const s = statSync(full);
      if (s.isDirectory()) {
        walk(full);
      } else if (s.isFile() && entry.endsWith('.ai.yaml')) {
        results.push(relative(root, full).split('\\').join('/'));
      }
    }
  }

  walk(root);
  return results.sort();
}

/**
 * Load exclude list; returns Set of relative paths (e.g. "options/bilingual.ai.yaml").
 */
function loadExcludes() {
  if (!existsSync(EXCLUDE_CONFIG)) return new Set();
  try {
    const content = readFileSync(EXCLUDE_CONFIG, 'utf8');
    const parsed = JSON.parse(content);
    const list = parsed?.excluded_files ?? [];
    return new Set(
      list
        .map((entry) => (typeof entry === 'string' ? entry : entry?.path))
        .filter(Boolean)
    );
  } catch (err) {
    console.error(`[check-bundle-parity] Failed to parse ${EXCLUDE_CONFIG}:`);
    console.error(err.message);
    process.exit(2);
  }
}

function main() {
  if (!existsSync(SOURCE_DIR)) {
    console.error(`[check-bundle-parity] Source directory not found: ${SOURCE_DIR}`);
    process.exit(2);
  }

  if (!existsSync(BUNDLED_AI_DIR)) {
    console.error(`[check-bundle-parity] Bundled directory not found: ${BUNDLED_AI_DIR}`);
    console.error('Run `npm run prepack` inside cli/ first so that bundled/ is generated.');
    process.exit(2);
  }

  const sourceFiles = collectAiYaml(SOURCE_DIR);
  const bundledAll = collectAiYaml(BUNDLED_AI_DIR);
  // Source maps to bundled/ai/standards/<file> for top-level and
  // bundled/ai/options/<subpath> for option files. Strip those prefixes
  // so that relative comparisons align with .standards/ layout.
  const bundledFiles = bundledAll.map((p) => {
    if (p.startsWith('standards/')) return p.slice('standards/'.length);
    return p; // options/... and other subtrees stay as-is
  }).sort();

  const excludes = loadExcludes();
  const sourceSet = new Set(sourceFiles.filter((p) => !excludes.has(p)));
  const bundledSet = new Set(bundledFiles.filter((p) => !excludes.has(p)));

  // Source present but missing in bundle:
  const missingFromBundle = [...sourceSet].filter((p) => !bundledSet.has(p)).sort();
  // Bundle present but missing in source (reverse drift):
  const missingFromSource = [...bundledSet].filter((p) => !sourceSet.has(p)).sort();

  const ok = missingFromBundle.length === 0 && missingFromSource.length === 0;

  if (JSON_OUTPUT) {
    const payload = {
      ok,
      source_count: sourceFiles.length,
      bundled_count: bundledFiles.length,
      excludes: [...excludes].sort(),
      missing_from_bundle: missingFromBundle,
      missing_from_source: missingFromSource
    };
    console.log(JSON.stringify(payload, null, 2));
    process.exit(ok ? 0 : 1);
  }

  console.log(`[check-bundle-parity] source=${sourceFiles.length} bundled=${bundledFiles.length} excludes=${excludes.size}`);
  if (ok) {
    console.log('[check-bundle-parity] OK — bundle parity holds (modulo excludes).');
    process.exit(0);
  }

  if (missingFromBundle.length > 0) {
    console.log('');
    console.log(`[check-bundle-parity] ${missingFromBundle.length} file(s) in source but NOT in bundle:`);
    for (const p of missingFromBundle) console.log(`  - ${p}`);
  }
  if (missingFromSource.length > 0) {
    console.log('');
    console.log(`[check-bundle-parity] ${missingFromSource.length} file(s) in bundle but NOT in source:`);
    for (const p of missingFromSource) console.log(`  - ${p}`);
  }
  console.log('');
  console.log('[check-bundle-parity] FAIL — fix by either:');
  console.log('  1. Updating cli/scripts/prepack.mjs copy rules, or');
  console.log('  2. Adding intentional exclusions to cli/scripts/bundle-exclude.json.');
  console.log('');
  console.log('See DEC-044 / XSPEC-071 for context.');
  process.exit(1);
}

main();
